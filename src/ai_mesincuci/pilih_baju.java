private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
    try {
        int jumlah = Integer.parseInt(txtJumlah.getText());
        if(jumlah <= 0) {
            JOptionPane.showMessageDialog(this, "Jumlah Pakaian Harus Di Atas 0..",
                    "NoStok", JOptionPane.WARNING_MESSAGE);
        }
        else if(jumlah > 15) {
            JOptionPane.showMessageDialog(this, "Jumlah Pakaian Harus Di Bawah 15..",
                    "StokOver", JOptionPane.WARNING_MESSAGE);
            txtJumlah.setText("0");
        }
        else {
            String model = "";
            if(rbCelana.isSelected()) {
                model = "C";
            }
            else if(rbJaket.isSelected()) {
                model = "J";
            }
            else {
                model = "K";
            }
            
            String warna = "";
            if(rbPutih.isSelected()) {
                warna = "P";
            }
            else {
                warna = "W";
            }
            
            String pudar = "";
            if(rbYa.isSelected()) {
                pudar = "Y";
            }
            else {
                pudar = "T";
            }
            
            javax.swing.table.DefaultTableModel Model = ((javax.swing.table.DefaultTableModel)tblData.getModel());
            Model.addRow(new String [] {
                   model,
                   jcbKain.getSelectedItem().toString().trim(),
                   jcbNoda.getSelectedItem().toString().trim(),
                   warna,
                   pudar,
                   txtJumlah.getText()
            });
            atur();
        }
    }
    catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Error Pada : \n"+e, "Tambah", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_tambahActionPerformed

private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
    try {
        int a = tblData.getSelectedRow();
        if(tblData.getRowCount() <= 0) {
            JOptionPane.showMessageDialog(this, "Data Belum Ada..",
                    "Hapus", JOptionPane.WARNING_MESSAGE);
        }
        else if(a<0) {
            JOptionPane.showMessageDialog(this, "Silahkan Pilih Baris Data Yang Akan Di Hapus..",
                    "Hapus", JOptionPane.WARNING_MESSAGE);
        }
        else {
            javax.swing.table.DefaultTableModel Model = ((javax.swing.table.DefaultTableModel)tblData.getModel());
            Model.removeRow(a);
            atur();
        }
    }
    catch(Exception e) {
        JOptionPane.showMessageDialog(this, "Error Pada : \n"+e, "Hapus", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_hapusActionPerformed

