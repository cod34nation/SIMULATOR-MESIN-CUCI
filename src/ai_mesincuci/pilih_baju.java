private void laundryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_laundryActionPerformed
    try {
        if(tblData.getRowCount() <= 0) {
             JOptionPane.showMessageDialog(this, "Data Belum Ada..",
                    "Laundry", JOptionPane.WARNING_MESSAGE);
        }
        else {
            String data[][] = new String [tblData.getRowCount()] [6];
            for (int i = 0; i < data.length; i++) {
                data[i][0] = tblData.getValueAt(i, 0).toString().trim();
                data[i][1] = tblData.getValueAt(i, 1).toString().trim();
                data[i][2] = tblData.getValueAt(i, 2).toString().trim();
                data[i][3] = tblData.getValueAt(i, 3).toString().trim();
                data[i][4] = tblData.getValueAt(i, 4).toString().trim();
                data[i][5] = tblData.getValueAt(i, 5).toString().trim();
            }            
            ls.set_laundry(data, this);
        }
    }
    catch(Exception e) {
        e.printStackTrace();
        //JOptionPane.showMessageDialog(this, "Error Pada : \n"+e, "Laundry", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_laundryActionPerformed
    
    public void awal() {
        try {
            rbKaos.setSelected(true);
            int a = jcbKain.getItemCount();
            jcbKain.removeAllItems();    
            for (int i = 0; i < baju.length; i++) {
                jcbKain.addItem(baju[i]);
            }
            jcbNoda.setSelectedIndex(0);
            rbKaos.setSelected(true);
            rbJaket.setSelected(false);
            rbCelana.setSelected(false);
            rbWarna.setSelected(true);
            rbPutih.setSelected(false);
            rbTidak.setSelected(true);
            rbYa.setSelected(false);
            txtJumlah.setText("0");
            
            javax.swing.table.DefaultTableModel Model = ((javax.swing.table.DefaultTableModel)tblData.getModel());
                int row = Model.getRowCount();
                for (int i = 0; i < row; i++) {
                    Model.removeRow(0);
                }
        }
        catch(Exception e) {
            JOptionPane.showMessageDialog(this, "Error Pada : \n"+e, "Awal", JOptionPane.ERROR_MESSAGE);
        }
    }
    
